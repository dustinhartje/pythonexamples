


# Join strings
string1 = 'foo'
string2 = 'bar'
joined_strings = '/'.join([string1, string2])
print(joined_strings)

# Split a string
split_strings = joined_strings.split('/')
print(split_strings)

# Slice a string
slicee = 'Hello World!'

## Substring between x and y
print(slicee[1:5])

## Everything before x
print(slicee[:5])

## Everything after x
print(slicee[1:])


